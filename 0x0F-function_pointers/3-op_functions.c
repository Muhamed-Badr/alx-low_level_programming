/**
 * op_add - Adds two integers.
 * @a: The 1st integer.
 * @b: The 2nd integer.
 *
 * Return: The sum of `a` and `b`.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtracts the 2nd integer from the 1st one.
 * @a: The 1st integer.
 * @b: The 2nd integer.
 *
 * Return: The difference of `a` and `b`.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies two integers.
 * @a: The 1st integer.
 * @b: The 2nd integer.
 *
 * Return: The product of `a` and `b`.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides the 2nd integer by the 1st one.
 * @a: The 1st integer (numerator/البسط).
 * @b: The 2nd integer (denominator/المقام).
 *
 * Return: The result of `a` divided by `b`.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Calculate the remainder of the division of two integers.
 * @a: The 1st integer.
 * @b: The 2nd integer.
 *
 * Return: The remainder of `a` divided by `b`.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
